#변수의 선언 및 할당 아래의 경우 x 에 2번의 할당을했다 이경우 마지막에 할당된 값만이 남으며
#이전에 할당시켯던 10이라는 값은 20에 의해 덮어쓰기된다 다시말해 10은 없어진다.
x = 10
print (x)
#같은 x 를출력시키지만 이번 출력이후 20으로 할당했기 때문에 기존 10이라는 값은 사라진다
x = 20
print (x)

#아래 같이 여러 함수의 할당을 동시에하는것이다
